Saldo= 1000
Saque= 200
Limite= 100
#Usando os Operadores de Comparação como base:
print(Saldo >= Saque)
print(Saque <= Limite)

#Este é o Operador "E" ou "and":
print(Saldo >= Saque and Saque <= Limite)
#Este é o Operador "OU" ou "or":
print(Saldo >= Saque or Saque <= Limite)
#Este é o Operador de negação "not":
contatos_emergencia= []
print(not 1000 > 1500)
